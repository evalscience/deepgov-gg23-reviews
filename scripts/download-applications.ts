async function fetchProjectData(projectIds: string[]) {
  const url = "https://beta.indexer.gitcoin.co/v1/graphql";

  const body = JSON.stringify({
    query: `
        query Application($projectIds: [String!]!) {
          applications(
            where: {
              projectId: { _in: $projectIds },
              status: { _eq: APPROVED }
              chainId: {_eq: 10 }
            }
          ) {
            projectId
            roundId
            chainId
            project {
              name
            }
            metadata
          }
        }`,
    variables: { projectIds },
    operationName: "Application",
  });

  const response = await fetch(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body,
  });

  if (!response.ok) {
    throw new Error(`Error fetching projects: ${response.statusText}`);
  }

  return response.json();
}

